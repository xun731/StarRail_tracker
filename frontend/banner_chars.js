/**
 * 把後端 /api/banners 回的原始日期 + BANNER_CHARS 合成完整 BANNER_HISTORY。
 *
 * 輸入（後端格式）：
 *   [{ version, phase, start, end, items: [decorative] }, ...]
 *
 * 輸出（BANNER_HISTORY 格式）：
 *   [{ type, start, end, up: [角色或光錐名, ...] }, ...]
 *
 * 流程：
 *   1. key = `${version} ${phase}`
 *   2. chars = BANNER_CHARS[key]  // 全部 UP 角色（新+復刻）
 *   3. 推一筆角色 banner: { type:'character', up: chars }
 *   4. 反查每個角色的簽名光錐（從 data.js HSR_LIGHT_CONES）
 *   5. 推一筆光錐 banner: { type:'light_cone', up: 對應的所有光錐 }
 */
function convertBannersToHistory(banners) {
  const out = [];
  const unmapped = [];
  const noLc = [];

  for (const b of banners) {
    const key = `${b.version} ${b.phase}`;
    const chars = BANNER_CHARS[key];
    if (!chars || chars.length === 0) {
      unmapped.push(key);
      continue;
    }

    // 角色 banner — 全部 UP 角色（含復刻）
    out.push({ type: 'character', start: b.start, end: b.end, up: [...chars] });

    // 光錐 banner — 用 data.js 反查每位角色的簽名光錐
    const lcs = chars
      .map(c => HSR_LIGHT_CONES.find(lc => lc.char === c)?.name)
      .filter(Boolean);
    if (lcs.length > 0) {
      out.push({ type: 'light_cone', start: b.start, end: b.end, up: lcs });
    }
    if (lcs.length < chars.length) {
      const missing = chars.filter(c => !HSR_LIGHT_CONES.find(lc => lc.char === c));
      noLc.push(`${key}: 缺光錐 [${missing.join(', ')}]`);
    }
  }

  if (unmapped.length) {
    console.warn(
      `[banner_chars] ${unmapped.length} 期 banner 後端有但 BANNER_CHARS 沒對應：\n  ` +
      unmapped.join('\n  ')
    );
  }
  if (noLc.length) {
    console.info(
      `[banner_chars] ${noLc.length} 期有角色在 data.js HSR_LIGHT_CONES 找不到簽名光錐（不影響運作）：\n  ` +
      noLc.join('\n  ')
    );
  }

  return out;
}
